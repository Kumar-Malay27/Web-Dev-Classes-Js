// post increment i++
// post decrement i--
// pre increment ++i
// pre decrement --i
for(let i=0;i<10;i++){
    console.log(i);
}

// while loop
let i=0;
while(i<10){
    console.log(i);
    i++;
}

// do while loop
let j=0;
do{
    console.log(j);
    j++;
}while(j<10);

